package com.syntax.class03;

public class TaskOne {
	
	public static void main(String[] args) {
		
		int num=100;
		
		num=num+500;
		System.out.println(num);
		
		num+=200;
		System.out.println(num); 
		
		num-=60;
		System.out.println(num);
		
		int cakePiece=11;
		cakePiece/=4;
		System.out.println(cakePiece);
		
		int cake=25;
		cake%=7;
		System.out.println(cake); // Declare variable cake=25 and divide cake between 7 people . 
		//Using shorthand operator found out how many pieces of cake left after it was distributed equally among 
		//7 people.
		
	}

}
